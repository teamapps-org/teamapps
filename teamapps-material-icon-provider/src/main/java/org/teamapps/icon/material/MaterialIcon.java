	private static MaterialIcon create(String name) {
		MaterialIcon icon = new MaterialIcon(name);
		ICONS_BY_NAME.put(name, icon);
		return icon;
	}

	public static Collection<MaterialIcon> getAllIcons() {
		return ICONS_BY_NAME.values();
	}

	public static MaterialIcon forName(String name) {
		return ICONS_BY_NAME.get(name);
	}

	private final String iconName;
	private final MaterialIconStyle style;

	private MaterialIcon(String iconName) {
		this(iconName, null);
	}

	private MaterialIcon(String iconName, MaterialIconStyle style) {
		this.iconName = iconName;
		this.style = style;
	}

	public String getIconName() {
		return iconName;
	}

	@Override
	public MaterialIcon withStyle(MaterialIconStyle style) {
		return new MaterialIcon(iconName, style);
	}

	@Override
	public MaterialIconStyle getStyle() {
		return style;
	}
